'''
Необходимо создать функцию strict, которая 
будет принимать два именованных аргумента, которые являются 
целыми числами: n и m, и возвращать их сумму. При этом если функция будет вызываться 
без названий аргументов, должна появляться ошибка.


'''

class Answer:
    def strict(self, *, n,m):
      
        return n+m
